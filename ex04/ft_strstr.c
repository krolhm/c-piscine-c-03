char	*ft_strstr(char *str, char *to_find)
{
	int	a;
	int	b;

	a = 0;
	while (str[a] != '\0')
	{
		b = 0;
		while (to_find[b] == str[a + b])
		{
			if (to_find[b + 1] == '\0')
				return (str + a);
		}
		b++;
	}
	a++;
	return (0);
}
