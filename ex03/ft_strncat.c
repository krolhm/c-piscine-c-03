char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	unsigned	int	a;
	unsigned	int	b;

	a = 0;
	while (dest[a] != '\0')
		a++;
	b = 0;
	while (src[b] != '\0' && b < nb)
	{
		dest[a + b] = src[b];
		b++;
	}
	dest[a + b] = '\0';
	return (dest);
}
